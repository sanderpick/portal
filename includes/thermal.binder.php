				<p>
					<strong>What is a Solar Thermal System?</strong>
				</p>
				<p>
					Solar thermal systems harness the sun’s energy and turn it into heat, otherwise known as thermal energy. The most common forms of solar thermal systems are low and medium temperature applications used on a residential scale for pool heating, domestic hot water heating, and space heating.<br><br>
				</p>
				<p>
					The basic operation of a solar thermal system works on the principles of thermodynamics and heat transfer. Heat is collected from the sun and transferred to a different location where heat is desired. Individual systems are engineered to properly collect and transfer energy at maximum efficiency to the location where the heat is needed.<br><br>
				</p>
				<p>
					The two main types of solar thermal systems are passive and active. Passive systems are generally used in warm climates where there is no threat of freezing. These types of systems typically incorporate an integrated tank on the collector or utilize what is called “thermo-siphoning” which operates on the principle that hotter, less dense fluids tend to rise. Active systems are used in climates that have the possibility of freezing. In active systems, a pump is used to circulate a heat transfer fluid through a solar thermal collector, which transfers the heat to a storage tank located in a conditioned space where there is no threat of freezing.<br><br>
				</p>
				<p>
					Solar thermal collectors have a much higher efficiency as compared to a solar electric (PV) collector. Typical efficiencies range from around 40-80 percent depending on the type of collector and system.<br><br>
				</p>
				<p>
					<strong>Can I Net-Meter a Solar Thermal System?</strong>
				</p>
				<p>
					Solar thermal systems produce hot water and not gas, there is no measurable return to the utility company and therefore no net-metering available. Because of this, the best paybacks from a solar thermal system are derived when a system is designed to offset a percentage of the total energy needed to heat water and not over-produce. The best way to measure savings is to compare previous gas bills to bills after a thermal system is installed, or install a production monitoring system.<br><br>
				</p>
