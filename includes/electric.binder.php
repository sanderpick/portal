					<strong>What is a Grid-Tied Solar Electric System?</strong>
				</p>
				<p>
					The main difference between a “grid-tied” solar electric system and an “off-grid” system is a bank of batteries for energy storage. Grid-tied systems use the utility company as an energy storage device instead of a large and heavy battery system. When energy is produced by your solar array, it is transferred through wiring to the inverter where it is turned into energy that can be used by the electrical devices in your home. If there are any of these electrical devices using energy while the solar system is producing, that energy will first go to those devices to be consumed. If any extra energy is being produced that your home is not consuming, that energy will be fed into the electrical grid and measured by what is called a net-meter. On cloudy days, at night, or any other time when additional energy is required and the solar system can’t meet the demand, additional energy is drawn from the electrical grid to supply the electrical loads in your home. The solar electric system has a “give and take” relationship with the electrical grid that is constantly being monitored by the net-meter.<br><br>
				</p>
				<p>
					<strong>What is a Net-Meter?</strong>
				</p>
				<p>
					The net-meter keeps track of how much energy your solar electric system puts into the grid vs. how much energy your home draws from the grid. When the solar system is providing more energy than your home needs, your net-meter “spins backward,” counting how much energy you are putting into the grid. When your home needs energy and the solar system is not producing the required amount, the energy will come from the electrical grid and your net-meter will “spin forward,” counting how much energy you are taking out of the grid.  The goal of a solar electric system is to provide a “net-zero” number on your meter, meaning your system provided exactly enough energy to power your home year round. If the system does not provide all of the energy your home needs, you will receive a bill from your utility company for any extra energy they supplied you. If your system provides more energy than your home needs, your utility company may provide you several options for paying or crediting you for your excess energy. A <strong>Lighthouse</strong>solar representative can advise you of your utility’s policies.<br><br>
				</p>
